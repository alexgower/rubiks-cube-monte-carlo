# Function to calculate the modal difference between E1 and E0 for each unique E0 value
function calculate_modal_difference(E0_values, E1_values)
    unique_E0 = sort(unique(E0_values))
    modal_differences = Float64[]
    
    for e0 in unique_E0
        differences = E1_values[E0_values .== e0] .- e0
        if !isempty(differences)
            h = fit(Histogram, differences, nbins=100)
            mode_index = argmax(h.weights)
            mode_value = (h.edges[1][mode_index] + h.edges[1][mode_index+1]) / 2
            push!(modal_differences, mode_value)
        end
    end
    
    return unique_E0, modal_differences
end

